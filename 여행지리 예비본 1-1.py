# 고대 로마의 건축물들에 대한 정보를 딕셔너리로 저장
roman_buildings = {
    "콜로세움": "콜로세움은 로마의 거대한 원형 경기장으로, 검투사 경기와 공개 처형 등이 열렸던 곳입니다.",
    "판테온": "판테온은 모든 신들을 모시는 신전으로, 돔과 오큘러스가 특징인 건축물입니다.",
    "로만 포럼": "로만 포럼은 고대 로마의 정치, 상업, 종교 중심지로 다양한 중요 건축물들이 모여 있는 곳입니다.",
    "카라칼라 목욕탕": "카라칼라 목욕탕은 로마 제국의 대규모 공중 목욕탕으로, 사치스럽고 복잡한 구조를 자랑합니다.",
    "트라야누스 기둥": "트라야누스 기둥은 트라야누스 황제의 다키아 전쟁 승리를 기념하기 위해 세워진 기념비로, 나선형 부조가 새겨져 있습니다."
}

def 소개_건축물():
    print("고대 로마의 건축물들에 대한 정보를 제공합니다.\n")
    for building in roman_buildings:
        print(building)
    
    while True:
        choice = input("\n정보를 알고 싶은 건축물의 이름을 입력하세요 (종료하려면 '종료' 입력): ")
        if choice == '종료':
            print("프로그램을 종료합니다.")
            break
        elif choice in roman_buildings:
            print(f"\n{choice}: {roman_buildings[choice]}\n")
        else:
            print("해당 건축물에 대한 정보가 없습니다. 다시 입력해주세요.")

# 프로그램 실행
소개_건축물()
